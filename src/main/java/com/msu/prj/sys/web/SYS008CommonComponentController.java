			Map <String, Object> outVar         = nxDto.getOutVariableMap();                                        			
			Map <String, DataSetMap> outDataset = nxDto.getOutDataSetMap();                                 					
	                                                                                                               				
			sys008CommonComponentService.saveCommonComponentList(inVar, inDataset, outVar, outDataset, sessionVO);                 	    
			                                                                                                					
